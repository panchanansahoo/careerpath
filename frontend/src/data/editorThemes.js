// Register all themes with Monaco
export function registerAllThemes(monaco) {
  EDITOR_THEMES.forEach(theme => {
    monaco.editor.defineTheme(theme.id, {
      base: theme.base,
      inherit: true,
      rules: theme.rules,
      colors: theme.colors,
    });
  });
}

// Get default theme from localStorage or fallback
export function getSavedTheme(storageKey = 'editor-theme') {
  return localStorage.getItem(storageKey) || 'one-dark-pro';
}

// Save theme to localStorage
export function saveTheme(themeId, storageKey = 'editor-theme') {
  localStorage.setItem(storageKey, themeId);
}
